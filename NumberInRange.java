package com.mluch.javastart.lesson4.hw;

public class NumberInRange {

    public static void main(String[] args) {
        int n = 150;

        if (n >= 100 & n <= 200) {
            System.out.println("true");
        } else {
            System.out.println("false");
        }
    }
}


//Input - Output
//n = 35    false
//n = 150    true